# ---------------------------------------------------------------------------
# HYBRID CONSTANTS
# ---------------------------------------------------------------------------
C_PUCT           = 1.414   # UCB exploration constant
TOP_K            = 3       # number of MCTS candidates handed to minimax
CANDIDATE_RADIUS = 2
MAX_CANDIDATES   = 15
MAX_ROLLOUT      = 30      # max moves per simulation before declaring draw

# Scoring constants (same as minimax agent)
SCORES = {1: 10, 2: 100, 3: 1_000, 4: 10_000, 5: 1_000_000}
WIN_SCORE  =  SCORES[5]
LOSE_SCORE = -SCORES[5]

DIRECTIONS = [
    (0, 1),   # horizontal
    (1, 0),   # vertical
    (1, 1),   # diagonal
    (1, -1),  # anti-diagonal
]


# ===========================================================================
# PART 1 — CANDIDATE MOVES  (same as minimax and MCTS agents)
# ===========================================================================

def get_candidate_moves(board: Board) -> list:
    """
    Return empty cells within CANDIDATE_RADIUS of any occupied cell.
    If the board is empty, return the center cell only.
    """
    occupied = list(zip(*board.grid.nonzero()))
    if not occupied:
        center = board.size // 2
        return [(center, center)]

    candidates = set()
    for (r, c) in occupied:
        for dr in range(-CANDIDATE_RADIUS, CANDIDATE_RADIUS + 1):
            for dc in range(-CANDIDATE_RADIUS, CANDIDATE_RADIUS + 1):
                nr, nc = r + dr, c + dc
                if (0 <= nr < board.size and
                        0 <= nc < board.size and
                        board.grid[nr, nc] == 0):
                    candidates.add((nr, nc))

    return list(candidates)


