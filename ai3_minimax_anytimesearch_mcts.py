def _rollout(board: Board, player: int) -> float:
    """
    Heuristic rollout — same implementation as mcts.py rollout().
    Returns float ∈ {0.0, 0.5, 1.0} from `player`'s perspective.

    HINT: copy your rollout() implementation from mcts.py here.

    INPUT:  board (copy — will be mutated), player
    OUTPUT: float
    """
    # ---------------------------------------------------------------------------
    # YOUR CODE HERE  (copy from mcts.py)
    # ---------------------------------------------------------------------------
    current_player = player
    for _ in range(MAX_ROLLOUT):
        candidates = get_candidate_moves(board)
        if not candidates:
            return 0.5

        # Check immediate win for current_player
        move = None
        for candidate in candidates:
            board.make_move(candidate[0], candidate[1], current_player)
            if board.check_win(candidate[0], candidate[1], current_player):
                board.undo_move(candidate[0], candidate[1])
                move = candidate
                break
            board.undo_move(candidate[0], candidate[1])

        # Check immediate block (opponent about to win)
        if move is None:
            opponent = 3 - current_player
            for candidate in candidates:
                board.make_move(candidate[0], candidate[1], opponent)
                if board.check_win(candidate[0], candidate[1], opponent):
                    board.undo_move(candidate[0], candidate[1])
                    move = candidate
                    break
                board.undo_move(candidate[0], candidate[1])

        # Otherwise random
        if move is None:
            move = random.choice(candidates)

        board.make_move(move[0], move[1], current_player)
        if board.check_win(move[0], move[1], current_player):
            return 1.0 if current_player == player else 0.0
        current_player = 3 - current_player

    return 0.5


class MCTSNode:
    """
    MCTS tree node — identical to mcts.py MCTSNode.
    Used in Phase 1 (MCTS exploration).
    """

    def __init__(self, board: Board, player: int,
                 parent: Optional["MCTSNode"] = None,
                 move: Optional[Tuple[int, int]] = None):
        self.board         = board
        self.player        = player
        self.parent        = parent
        self.move          = move
        self.children      : list["MCTSNode"] = []
        self.visits        : int   = 0
        self.wins          : float = 0.0
        self.untried_moves : list  = get_candidate_moves(board)[:MAX_CANDIDATES]

    def is_fully_expanded(self) -> bool:
        return len(self.untried_moves) == 0

    def is_terminal(self) -> bool:
        """Same as mcts.py MCTSNode.is_terminal()."""
        # ---------------------------------------------------------------------------
        # YOUR CODE HERE  (copy from mcts.py)
        # ---------------------------------------------------------------------------
        if self.move is None:
            return False
        if self.board.check_win(self.move[0], self.move[1], 3 - self.player) or self.board.is_full():
            return True
        return False

    def ucb_score(self, c: float = C_PUCT) -> float:
        """Same as mcts.py MCTSNode.ucb_score()."""
        # ---------------------------------------------------------------------------
        # YOUR CODE HERE  (copy from mcts.py)
        # ---------------------------------------------------------------------------
        visits = self.visits
        wins = self.wins
        if visits == 0:
            return math.inf
        return (wins / visits) + c * math.sqrt(math.log(self.parent.visits) / visits)

    def best_child(self, c: float = C_PUCT) -> "MCTSNode":
        """Return child with highest UCB score."""
        return max(self.children, key=lambda child: child.ucb_score(c))

    def expand(self) -> "MCTSNode":
        """Same as mcts.py MCTSNode.expand()."""
        # ---------------------------------------------------------------------------
        # YOUR CODE HERE  (copy from mcts.py)
        # ---------------------------------------------------------------------------
        move = self.untried_moves.pop()
        move = (int(move[0]), int(move[1]))
        new_board = self.board.copy()
        new_board.make_move(move[0], move[1], self.player)
        child = MCTSNode(new_board, 3 - self.player, self, move)
        self.children.append(child)
        return child

    def backpropagate(self, result: float) -> None:
        """Same as mcts.py MCTSNode.backpropagate()."""
        # ---------------------------------------------------------------------------
        # YOUR CODE HERE  (copy from mcts.py)
        # ---------------------------------------------------------------------------
        self.visits += 1
        self.wins += result
        if self.parent:
            self.parent.backpropagate(1 - result)


