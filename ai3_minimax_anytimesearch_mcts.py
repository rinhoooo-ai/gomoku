def hybrid_search(board: Board, player: int,
                  time_limit: float = 15.0) -> Tuple[int, int]:
    """
    Hybrid MCTS + Minimax search.

    Pipeline:
        1. Pre-check immediate win or block → return instantly if found.
        2. Phase 1 — MCTS exploration    (first half of time_limit)
        3. Phase 2 — Minimax refinement  (second half of time_limit)
        4. Return best move from Phase 2.

    Time split:
        phase1_budget = time_limit * 0.5
        phase2_budget = time_limit * 0.5   (split equally among TOP_K nodes)

    INPUT:
        board      : Board
        player     : 1 or 2
        time_limit : total wall-clock budget in seconds (default 15s)
    OUTPUT:
        Tuple[int, int] — best (r, c) move
    """
    start = time.time()

    # Pre-check: immediate win or block
    candidates = get_candidate_moves(board)
    for move in candidates:
        board.make_move(move[0], move[1], player)
        if board.check_win(move[0], move[1], player):
            board.undo_move(move[0], move[1])
            return move
        board.undo_move(move[0], move[1])

    opponent = 3 - player
    for move in candidates:
        board.make_move(move[0], move[1], opponent)
        if board.check_win(move[0], move[1], opponent):
            board.undo_move(move[0], move[1])
            return move
        board.undo_move(move[0], move[1])

    # ---------------------------------------------------------------------------
    # YOUR CODE HERE  (~8 lines)
    # Split time_limit into phase1_budget and phase2_budget.
    # Call mcts_phase() then minimax_phase() and return result.
    # ---------------------------------------------------------------------------
    phase1_budget = time_limit * 0.5
    phase2_budget = time_limit * 0.5
    top_k = mcts_phase(board, player, phase1_budget)
    move = minimax_phase(board, player, top_k, phase2_budget)
    return move


