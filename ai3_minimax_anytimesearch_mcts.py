def _minimax(board: Board, depth: int, alpha: float, beta: float,
             is_maximizing: bool, player: int,
             last_move: Optional[Tuple[int, int]],
             start: float, time_limit: float) -> Optional[int]:
    """
    Minimax with alpha-beta + time guard — same as minimax.py _minimax().

    Returns None when time budget is exhausted.

    HINT: copy your _minimax() implementation from minimax.py here.

    INPUT:  board, depth, alpha, beta, is_maximizing, player,
            last_move, start, time_limit
    OUTPUT: int or None
    """
    # ---------------------------------------------------------------------------
    # YOUR CODE HERE  (copy from minimax.py)
    # ---------------------------------------------------------------------------
    opponent = 3 - player

    # time
    if time.time() - start >= time_limit:
        return None

    # win check
    if last_move is not None:
        last_player = player if is_maximizing else opponent
        if board.check_win(last_move[0], last_move[1], last_player):
            return WIN_SCORE if last_player == player else LOSE_SCORE

    # depth limit
    if depth == 0:
        return evaluate(board, player)

    # draw
    if board.is_full():
        return 0

    sorted_moves = get_candidate_moves(board)[:MAX_CANDIDATES]

    if is_maximizing:
        best = -math.inf
        for (r, c) in sorted_moves:
            board.make_move(r, c, player)
            score = _minimax(board, depth - 1, alpha, beta, False, player, (r, c), start, time_limit)
            board.undo_move(r, c)

            if score is None:
                return None
            best = max(best, score)
            alpha = max(alpha, best)
            if beta <= alpha:
                break

        return best

    else:
        best = math.inf
        for (r, c) in sorted_moves:
            # mirror of maximizer block above
            board.make_move(r, c, opponent)
            score = _minimax(board, depth - 1, alpha, beta, True, player, (r, c), start, time_limit)
            board.undo_move(r, c)

            if score is None:
                return None
            best = min(best, score)
            beta = min(beta, best)
            if beta <= alpha:
                break

        return best



def minimax_phase(board: Board, player: int,
                  top_k_nodes: list,
                  time_budget: float) -> Tuple[int, int]:
    """
    Refine top-K MCTS candidates with iterative-deepening minimax.

    Algorithm:
        time_per_node = time_budget / len(top_k_nodes)

        For each node in top_k_nodes:
            Run iterative deepening minimax from node.board
            with budget = time_per_node.
            Record best_score and best_depth reached.

        Return the move of the node with the highest minimax score.

    Iterative deepening per node:
        best_score = -inf
        for depth in range(1, MAX_DEPTH + 1):
            if time expired for this node: break
            score = _minimax(node.board, depth, -inf, +inf,
                             False,   ← opponent moves next (node.player)
                             player,  ← we are always MAX
                             node.move, start_for_this_node, time_per_node)
            if score is None: break   ← time ran out mid-search
            best_score = score        ← completed depth, update

    INPUT:
        board        : Board (original, used only for fallback)
        player       : 1 or 2
        top_k_nodes  : list of MCTSNode from mcts_phase()
        time_budget  : seconds allocated to minimax phase
    OUTPUT:
        Tuple[int, int] — move with highest refined minimax score

    CONSTANTS you may use:
        MAX_DEPTH = 20
    """
    MAX_DEPTH = 20

    if not top_k_nodes:
        # Fallback: no MCTS candidates, pick first legal move
        return get_candidate_moves(board)[0]

    # ---------------------------------------------------------------------------
    # YOUR CODE HERE  (~30 lines)
    # ---------------------------------------------------------------------------
    time_per_node = time_budget / len(top_k_nodes)
    best_score = -math.inf
    best_move = None
    start = time.time()
    best_overall_score = -math.inf
    best_move = top_k_nodes[0].move  # fallback

    for node in top_k_nodes:
        node_best_score = -math.inf
        start_node = time.time()
        for depth in range(1, MAX_DEPTH + 1):
            if time.time() - start_node >= time_per_node:
                break
            score = _minimax(node.board, depth, -math.inf, math.inf,
                            False, player, node.move, start_node, time_per_node)
            if score is None:
                break
            node_best_score = score

        if node_best_score > best_overall_score:
            best_overall_score = node_best_score
            best_move = node.move

    return best_move


