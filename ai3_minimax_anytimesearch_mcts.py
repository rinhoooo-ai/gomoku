def mcts_phase(board: Board, player: int,
               time_budget: float) -> list:
    """
    Run MCTS for `time_budget` seconds and return the top-K children
    of root sorted by visit count (descending).

    This is identical to mcts_search() main loop, but:
        - Uses `time_budget` instead of `time_limit`
        - Returns top-K children instead of the single best move

    Algorithm:
        root = MCTSNode(board.copy(), player)
        Run standard 4-phase MCTS loop until time_budget expires.
        Return sorted(root.children, by visits, descending)[:TOP_K]

    INPUT:
        board       : Board
        player      : 1 or 2
        time_budget : seconds allocated to MCTS phase
    OUTPUT:
        list of MCTSNode — top-K children by visit count
        (may be shorter than TOP_K if tree has fewer children)

    HINT: print MCTS iteration count at the end for debugging.
    """
    root  = MCTSNode(board.copy(), player)
    start = time.time()
    iterations = 0

    while time.time() - start < time_budget:
        # ---------------------------------------------------------------------------
        # YOUR CODE HERE  (~15 lines, same as mcts_search loop)
        # ---------------------------------------------------------------------------
        node = root
        while node.is_fully_expanded() and not node.is_terminal():
            node = node.best_child()

        if not node.is_terminal():
            node = node.expand()
            result = _rollout(node.board, node.player)
            node.backpropagate(result)
        else:
            result = 0.5
            node.backpropagate(result)
        iterations += 1

    print(f"[Phase 1] MCTS iterations: {iterations}")
    top_k = sorted(root.children, key=lambda c: c.visits, reverse=True)[:TOP_K]
    return top_k


