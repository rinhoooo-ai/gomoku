# PART 2 — HEURISTIC EVALUATION  (same as minimax agent)
# ===========================================================================

def score_window(p_cnt: int, o_cnt: int) -> int:
    """Score a single 5-cell window for ONE player."""
    if p_cnt > 0 and o_cnt == 0:
        return SCORES[p_cnt]
    return 0


def evaluate(board: Board, player: int) -> int:
    """
    Heuristic evaluation of the full board for `player`.
    Scans all 5-cell windows in all 4 directions.

    INPUT:  board, player
    OUTPUT: int — positive = good for player, negative = bad
    """
    opponent = 3 - player
    score    = 0

    for (dr, dc) in DIRECTIONS:
        for r in range(board.size):
            for c in range(board.size):
                er, ec = r + 4 * dr, c + 4 * dc
                if not (0 <= er < board.size and 0 <= ec < board.size):
                    continue

                p_cnt = 0
                o_cnt = 0
                for i in range(5):
                    cell = board.grid[r + i * dr, c + i * dc]
                    if cell == player:
                        p_cnt += 1
                    elif cell == opponent:
                        o_cnt += 1

                score += score_window(p_cnt, o_cnt)
                score -= score_window(o_cnt, p_cnt)

    return score


