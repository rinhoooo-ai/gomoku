# PART 3 — ROLLOUT  (random simulation from a given state)
# ===========================================================================

def rollout(board: Board, player: int) -> float:
    """
    Simulate a random game from the given board state and return the result
    from `player`'s perspective.

    Algorithm:
        current_player = player
        for up to MAX_ROLLOUT steps:
            candidates = get_candidate_moves(board)
            if no candidates: return 0.5  (draw)

            HINT: add a simple heuristic — before picking randomly,
                  check if any candidate is an immediate win for current_player
                  or an immediate block (opponent about to win).
                  If found, play that move instead of random.
                  This significantly improves rollout quality without a network.

            pick a move (random or heuristic), apply it
            if current_player wins: return 1.0 if current_player == player else 0.0
            flip current_player

        return 0.5  (draw / horizon)

    INPUT:
        board   — Board (will be MUTATED then restored; make a copy before calling)
        player  — the perspective player (1 or 2)
    OUTPUT:
        float — 1.0 (player wins), 0.0 (player loses), 0.5 (draw)

    NOTE: caller is responsible for passing a COPY of the board.
    """
    # ---------------------------------------------------------------------------
    # YOUR CODE HERE
    # ---------------------------------------------------------------------------
    current_player = player
    for _ in range(MAX_ROLLOUT):
        candidates = get_candidate_moves(board)
        if not candidates:
            return 0.5

        # Check immediate win for current_player
        move = None
        for candidate in candidates:
            board.make_move(candidate[0], candidate[1], current_player)
            if board.check_win(candidate[0], candidate[1], current_player):
                board.undo_move(candidate[0], candidate[1])
                move = candidate
                break
            board.undo_move(candidate[0], candidate[1])

        # Check immediate block (opponent about to win)
        if move is None:
            opponent = 3 - current_player
            for candidate in candidates:
                board.make_move(candidate[0], candidate[1], opponent)
                if board.check_win(candidate[0], candidate[1], opponent):
                    board.undo_move(candidate[0], candidate[1])
                    move = candidate
                    break
                board.undo_move(candidate[0], candidate[1])

        # Otherwise random
        if move is None:
            move = random.choice(candidates)

        board.make_move(move[0], move[1], current_player)
        if board.check_win(move[0], move[1], current_player):
            return 1.0 if current_player == player else 0.0
        current_player = 3 - current_player

    return 0.5


