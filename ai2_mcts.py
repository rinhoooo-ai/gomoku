def mcts_search(board: Board, player: int,
                time_limit: float = 15.0) -> Tuple[int, int]:
    """
    Run MCTS from the given board state and return the best move.

    Algorithm (standard 4-phase loop):
        root = MCTSNode(board_copy, player)

        while time not exceeded:
            # 1. SELECTION
            #    Start at root. While node is fully expanded and not terminal,
            #    descend via best_child(C_PUCT).

            # 2. EXPANSION
            #    If node is not terminal and not fully expanded,
            #    call node.expand() to get a new child node.

            # 3. SIMULATION
            #    Run rollout() from a COPY of the expanded node's board,
            #    with the expanded node's player to move.

            # 4. BACKPROPAGATION
            #    Call node.backpropagate(result).
            #    result is from the perspective of the player who just moved
            #    INTO the expanded node (i.e. expanded_node.parent.player).

        # SELECT BEST MOVE
        # Return the move of the child of root with the MOST visits
        # (not highest win rate — visit count is more robust).

    INPUT:
        board      : Board
        player     : 1 or 2
        time_limit : wall-clock budget in seconds
    OUTPUT:
        Tuple[int, int] — best (r, c) move

    HINT: print iteration count and best move stats at the end for debugging.
    """
    root = MCTSNode(board.copy(), player)
    start = time.time()
    iterations = 0

    # Pre-check: immediate win or block before MCTS
    candidates = get_candidate_moves(board)
    for move in candidates:
        board.make_move(move[0], move[1], player)
        if board.check_win(move[0], move[1], player):
            board.undo_move(move[0], move[1])
            return move
        board.undo_move(move[0], move[1])

    opponent = 3 - player
    for move in candidates:
        board.make_move(move[0], move[1], opponent)
        if board.check_win(move[0], move[1], opponent):
            board.undo_move(move[0], move[1])
            return move
        board.undo_move(move[0], move[1])

    while time.time() - start < time_limit:
        # ---------------------------------------------------------------------------
        # YOUR CODE HERE  (~20 lines)
        # ---------------------------------------------------------------------------
        node = root
        while node.is_fully_expanded() and not node.is_terminal():
            node = node.best_child()

        if not node.is_terminal():
            node = node.expand()
            result = rollout(node.board.copy(), node.player)
            node.backpropagate(result)
        else:
            result = 0.5
            node.backpropagate(result)
        iterations += 1

    # ---------------------------------------------------------------------------
    # YOUR CODE HERE — select and return best move  (~3 lines)
    # ---------------------------------------------------------------------------
    best_child = root.children[0]
    for child in root.children:
        if child.visits > best_child.visits:
            best_child = child
    print(f"MCTS iterations: {iterations}")
    print(f"Best move: {best_child.move}")
    return best_child.move


