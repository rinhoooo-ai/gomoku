# PART 2 — NODE
# ===========================================================================

class MCTSNode:
    """
    A single node in the MCTS tree.

    Attributes
    ----------
    board       : current board state at this node
    player      : the player whose turn it is TO MOVE from this node
    parent      : parent MCTSNode, or None if root
    move        : the (r, c) move that led FROM parent TO this node
    children    : list of MCTSNode - expanded children
    visits      : N(s) - how many times this node was visited
    wins        : W(s) - total win score accumulated via backprop
                  (wins are always from the perspective of the player
                   who MADE the move that created this node, i.e. parent.player)
    untried_moves : moves not yet expanded (initially all candidates)
    """

    def __init__(self, board: Board, player: int,
                 parent: Optional["MCTSNode"] = None,
                 move: Optional[Tuple[int, int]] = None):
        self.board         = board
        self.player        = player
        self.parent        = parent
        self.move          = move
        self.children      : list["MCTSNode"] = []
        self.visits        : int   = 0
        self.wins          : float = 0.0
        self.untried_moves : list  = get_candidate_moves(board)[:MAX_CANDIDATES]

    def is_fully_expanded(self) -> bool:
        """Return True when every candidate move has been expanded."""
        return len(self.untried_moves) == 0

    def is_terminal(self) -> bool:
        """
        Return True if this node represents a finished game state.
        A node is terminal if:
            - the move that created it was a winning move, OR
            - the board is full (draw)

        HINT: use board.check_win(r, c, last_player) where last_player
              is the opponent of self.player (they just moved).
              Be careful: if self.move is None (root), it's never terminal.

        INPUT:  self
        OUTPUT: bool
        """
        # ---------------------------------------------------------------------------
        # YOUR CODE HERE
        # ---------------------------------------------------------------------------
        if self.move is None:
            return False
        if self.board.check_win(self.move[0], self.move[1], 3 - self.player) or self.board.is_full():
            return True
        return False


    def ucb_score(self, c: float = C_PUCT) -> float:
        """
        UCB1 score for this node (used by parent to select which child to visit).

            UCB1 = (wins / visits) + c * sqrt(ln(parent.visits) / visits)

        Return +inf if visits == 0 (unvisited node always gets priority).

        INPUT:  self, c — exploration constant
        OUTPUT: float
        """
        # ---------------------------------------------------------------------------
        # YOUR CODE HERE
        # ---------------------------------------------------------------------------
        visits = self.visits
        wins = self.wins
        if visits == 0:
            return math.inf
        return (wins / visits) + c * math.sqrt(math.log(self.parent.visits) / visits)

    def best_child(self, c: float = C_PUCT) -> "MCTSNode":
        """
        Return the child with the highest UCB score.

        INPUT:  self, c
        OUTPUT: MCTSNode
        """
        # ---------------------------------------------------------------------------
        # YOUR CODE HERE  (~1 line)
        # ---------------------------------------------------------------------------
        return max(self.children, key=lambda child: child.ucb_score(c))

    def expand(self) -> "MCTSNode":
        """
        Pop one move from untried_moves, create a child node for it, and
        append it to self.children.

        Steps:
            1. Pop a random move from self.untried_moves.
            2. Copy self.board and apply the move for self.player.
            3. Create a new MCTSNode with:
                   board  = new board copy
                   player = opponent (3 - self.player)
                   parent = self
                   move   = the popped move
            4. Append to self.children and return the new node.

        INPUT:  self
        OUTPUT: MCTSNode — the newly created child
        """
        # ---------------------------------------------------------------------------
        # YOUR CODE HERE  (~6 lines)
        # ---------------------------------------------------------------------------
        move = self.untried_moves.pop()
        move = (int(move[0]), int(move[1]))
        new_board = self.board.copy()
        new_board.make_move(move[0], move[1], self.player)
        child = MCTSNode(new_board, 3 - self.player, self, move)
        self.children.append(child)
        return child

    def backpropagate(self, result: float) -> None:
        """
        Walk from this node to the root, updating visits and wins.

        `result` is from the perspective of the player who MADE the move
        leading to this node (i.e. self.parent.player if parent exists).

            self.visits += 1
            self.wins   += result
            if self.parent:
                self.parent.backpropagate(1 - result)   # flip perspective

        The flip `1 - result` works because result ∈ {0, 0.5, 1}:
            win for mover  → 1    → parent sees 0   (bad for them)
            draw           → 0.5  → parent sees 0.5 (neutral)
            loss for mover → 0    → parent sees 1   (good for them)

        INPUT:  self, result — float ∈ {0, 0.5, 1}
        OUTPUT: None  (mutates nodes in place)
        """
        # ---------------------------------------------------------------------------
        # YOUR CODE HERE  (~4 lines)
        # ---------------------------------------------------------------------------
        self.visits += 1
        self.wins += result
        if self.parent:
            self.parent.backpropagate(1 - result)



