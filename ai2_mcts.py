# ---------------------------------------------------------------------------
# MCTS CONSTANTS
# ---------------------------------------------------------------------------
C_PUCT      = 1.414   # UCB exploration constant  (sqrt(2) is standard)
MAX_ROLLOUT = 30      # max moves per simulation before declaring draw
CANDIDATE_RADIUS = 2  # same candidate pruning as minimax agent
MAX_CANDIDATES   = 15


# ===========================================================================
# PART 1 — CANDIDATE MOVES  (reuse same logic as minimax agent)
# ===========================================================================

def get_candidate_moves(board: Board) -> list:
    """
    Return empty cells within CANDIDATE_RADIUS of any occupied cell.
    If the board is empty, return the center cell only.

    INPUT:  board
    OUTPUT: list of (r, c) tuples
    """
    occupied = list(zip(*board.grid.nonzero()))
    if not occupied:
        center = board.size // 2
        return [(center, center)]

    candidates = set()
    for (r, c) in occupied:
        for dr in range(-CANDIDATE_RADIUS, CANDIDATE_RADIUS + 1):
            for dc in range(-CANDIDATE_RADIUS, CANDIDATE_RADIUS + 1):
                nr, nc = r + dr, c + dc
                if (0 <= nr < board.size and
                        0 <= nc < board.size and
                        board.grid[nr, nc] == 0):
                    candidates.add((nr, nc))

    return list(candidates)


